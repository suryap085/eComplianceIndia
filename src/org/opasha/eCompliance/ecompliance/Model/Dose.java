package org.opasha.eCompliance.ecompliance.Model;

public class Dose {
	
	public String doseType;
	public long doseDate;
	public int regimenId;

	public void setPatient(String doseType, long doseDate) {
		this.doseType = doseType;
		this.doseDate = doseDate;

	}

	public void setDoses(String doseType, long doseDate, int regimenId) {
		this.doseType = doseType;
		this.doseDate = doseDate;
		this.regimenId = regimenId;

	}

}
