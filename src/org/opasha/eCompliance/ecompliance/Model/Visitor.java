package org.opasha.eCompliance.ecompliance.Model;

public class Visitor {

	public String visitorID;
	public String name;
	public String visitorType;
	public long registrationDate;
	public String status;
	public String phone;
	public boolean isAuthenticated;
	public long creationTimeStamp;
	public String createdBy;
	public String tabId;
	public boolean isDeleted;

	public long loginTimeStamp;
	public String loginMachineID;

	public String scan;

	public void setVisitor(String visitorID, String name, String visitorType,
			long registrationDate, String status, String phone,
			boolean isAuthenticated, long creationTimeStamp, String createdBy,
			boolean isDeleted, String tabId) {
		this.visitorID = visitorID;
		this.name = name;
		this.visitorType = visitorType;
		this.registrationDate = registrationDate;
		this.status = status;
		this.phone = phone;
		this.isAuthenticated = isAuthenticated;
		this.creationTimeStamp = creationTimeStamp;
		this.createdBy = createdBy;
		this.isDeleted = isDeleted;
		this.tabId = tabId;
	}

	public void setVisitor(String visitorID, String name, String visitorType,
			long registrationDate, String status, String phone,
			boolean isAuthenticated, long creationTimeStamp, String createdBy,
			long loginTime, String machineId, String tabId) {
		this.visitorID = visitorID;
		this.name = name;
		this.visitorType = visitorType;
		this.registrationDate = registrationDate;
		this.status = status;
		this.phone = phone;
		this.isAuthenticated = isAuthenticated;
		this.creationTimeStamp = creationTimeStamp;
		this.createdBy = createdBy;
		this.loginTimeStamp = loginTime;
		this.loginMachineID = machineId;

	}
}
